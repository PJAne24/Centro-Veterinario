    private void BotonParaBajaPanelBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonParaBajaPanelBajaActionPerformed
        // TODO add your handling code here:
        int index = ListadoAnimalesPanelBaja.getSelectedIndex();
        if (index != -1) {
            Animal animalSeleccionado = null;
            String animalString = ListadoAnimalesPanelBaja.getSelectedValue();
            for (Mamífero m : listaMamíferos) {
                if (m.toString().equals(animalString)) {
                    animalSeleccionado = m;
                    listaMamíferos.remove(m);
                    break;
                }
            }
            if (animalSeleccionado == null) {
                for (Ave a : listaAves) {
                    if (a.toString().equals(animalString)) {
                        animalSeleccionado = a;
                        listaAves.remove(a);
                        break;
                    }
                }
            }
            if (animalSeleccionado == null) {
                for (Reptil r : listaReptiles) {
                    if (r.toString().equals(animalString)) {
                        animalSeleccionado = r;
                        listaReptiles.remove(r);
                        break;
                    }
                }
            }
            if (animalSeleccionado != null) {
                animalSeleccionado.setFechaEntrada(new Date()); // Actualizar fecha de entrada del animal
                listaBajas.add(animalSeleccionado);
                DefaultListModel modeloBajas = new DefaultListModel();
                for (Animal a : listaBajas) {
                    modeloBajas.addElement(a.toString());
                }
                ListadoBajasPanelBaja.setModel(modeloBajas);
                DefaultListModel modeloAnimales = (DefaultListModel) ListadoAnimalesPanelBaja.getModel();
                modeloAnimales.remove(index);
            } else {
                JOptionPane.showMessageDialog(null, "Error al dar de baja el animal");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un animal del listado de animales guardados para dar de baja");
        }

    }//GEN-LAST:event_BotonParaBajaPanelBajaActionPerformed

