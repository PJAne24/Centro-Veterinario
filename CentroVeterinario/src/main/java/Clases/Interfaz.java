    private void BotónGuardarAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotónGuardarAltaActionPerformed
        // TODO add your handling code here:
        // Obtener los valores de los campos
        String id = TextFieldID.getText();
        String tipo = TipoAnimalComboBox.getSelectedItem().toString();
        String nombre = TextFieldNombre.getText();
        String especie = TextFieldEspecie.getText();
        String peso = TextFieldPeso.getText();
        String tipoLesion = TextFieldTipoLesion.getText();
        String TratamientoInicial = TextFieldTratamientoInicial.getText();
        String gravedadLesion = BotonGravedadAlta.isSelected() ? "Alta" : "Baja";
        Date fechaEntrada = new Date();
        String veterinario = TextFieldVeterinario.getText();
        boolean motivoLesionAtropello = MotivoAtropello.isSelected();
        boolean motivoLesionCazaFurtiva = MotivoCazaFurtiva.isSelected();
        boolean motivoLesionInfeccionBacteriana = MotivoInfeccion.isSelected();

// Verificar si todos los campos están llenos
        if (tipo.equals("") || TratamientoInicial.equals("") || nombre.equals("") || especie.equals("") || peso.equals("") || tipoLesion.equals("") || gravedadLesion.equals("") || veterinario.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, rellene todos los campos para guardar un nuevo animal.", "Campos incompletos", JOptionPane.WARNING_MESSAGE);
        } else {
            Animal animal = null;
            switch (tipo) {
                case "Mamífero":
                    animal = new Mamífero(motivoLesionAtropello, id, tipo, nombre, especie, peso, tipoLesion, gravedadLesion, fechaEntrada, TratamientoInicial, veterinario);
                    ((Mamífero) animal).setMotivoLesionAtropello(motivoLesionAtropello);
                    break;
                case "Ave":
                    animal = new Ave(motivoLesionCazaFurtiva, id, tipo, nombre, especie, peso, tipoLesion, gravedadLesion, fechaEntrada, TratamientoInicial, veterinario);
                    ((Ave) animal).setMotivoLesionCazaFurtiva(motivoLesionCazaFurtiva);
                    break;
                case "Reptil":
                    animal = new Reptil(motivoLesionInfeccionBacteriana, id, tipo, nombre, especie, peso, tipoLesion, gravedadLesion, fechaEntrada, TratamientoInicial, veterinario);
                    ((Reptil) animal).setMotivoLesionInfeccionBacteriana(motivoLesionInfeccionBacteriana);
                    break;
            }
            if (animal != null) {
                // Agregar el nuevo animal al ArrayList correspondiente
                switch (tipo) {
                    case "Mamífero":
                        listaMamíferos.add((Mamífero) animal);
                        break;
                    case "Ave":
                        listaAves.add((Ave) animal);
                        break;
                    case "Reptil":
                        listaReptiles.add((Reptil) animal);
                        break;
                }
                // Mostrar mensaje de éxito
                JOptionPane.showMessageDialog(null, "Animal guardado exitosamente.", "Guardado exitoso", JOptionPane.INFORMATION_MESSAGE);
                // Limpiar los campos
                TextFieldID.setText("");
                TextFieldNombre.setText("");
                TextFieldEspecie.setText("");
                TextFieldPeso.setText("");
                TextFieldTipoLesion.setText("");
                BotonGravedadAlta.setSelected(true);
                TextFieldTratamientoInicial.setText("");
                TextFieldVeterinario.setText("");
                MotivoAtropello.setSelected(false);
                MotivoCazaFurtiva.setSelected(false);
                MotivoInfeccion.setSelected(false);
            }
        }
    }//GEN-LAST:event_BotónGuardarAltaActionPerformed

